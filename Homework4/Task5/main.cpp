#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    // Размеры первой коробки
    int a = 0, b = 0, c = 0;
    // Размеры второй коробки
    int m = 0, n = 0, k = 0;

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Проверка размещения одной коробки внутри другой ===\n\n";
    
    std::cout << "Введите размеры первой коробки (A B C) через пробел: ";
    std::cin >> a >> b >> c;
    
    std::cout << "Введите размеры второй коробки (M N K) через пробел: ";
    std::cin >> m >> n >> k;

    std::cout << "-----Проверяем-----\n";

    // Обработка некорректного ввода
    if (a <= 0 || b <= 0 || c <= 0 || m <= 0 || n <= 0 || k <= 0) {
        std::cout << "Ошибка! Размеры коробок должны быть положительными числами.\n";
        return 1;
    }

    // Сортируем размеры первой коробки по возрастанию (чтобы a <= b <= c)
    int temp;
    if (a > b) { temp = a; a = b; b = temp; }
    if (b > c) { temp = b; b = c; c = temp; }
    if (a > b) { temp = a; a = b; b = temp; }

    // Сортируем размеры второй коробки по возрастанию (чтобы m <= n <= k)
    if (m > n) { temp = m; m = n; n = temp; }
    if (n > k) { temp = n; n = k; k = temp; }
    if (m > n) { temp = m; m = n; n = temp; }

    // Проверяем, помещается ли отсортированная первая коробка во вторую по всем трем осям
    if (a < m && b < n && c < k) {
        std::cout << "Да, первую коробку можно положить внутрь второй!\n";
    } else {
        std::cout << "Нет, первая коробка не поместится внутрь второй.\n";
    }

    return 0;
}
