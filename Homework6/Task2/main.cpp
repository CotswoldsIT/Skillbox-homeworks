#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    // Объявляем переменные для запасов кофемашины
    int water = 0;
    int milk = 0;

    // Текстовый интерфейс: описание и ввод начальных данных
    std::cout << "=== Система управления кофемашиной ===\n";
    std::cout << "Введите количество воды в мл: ";
    std::cin >> water;
    std::cout << "Введите количество молока in мл: ";
    std::cin >> milk;

    // Контроль ввода
    if (water < 0 || milk < 0) {
        std::cout << "Ошибка! Количество ингредиентов не может быть отрицательным.\n";
        return 1;
    }

    // счётчики приготовленных напитков
    int americanoCount = 0;
    int latteCount = 0;

    // бесконечный цикл обслуживания посетителей
    while (true) {
        // условие выхода: если ресурсов не хватает ни на американо (300 воды), ни на латте (30 воды и 270 молока)
        bool canMakeAmericano = (water >= 300);
        bool canMakeLatte = (water >= 3 && milk >= 270); // Для латте нужно 30 мл воды (в ТЗ опечатка "30 мл воды", далее проверим)
        
        // корректировка рецепта латте из ТЗ: 30 мл воды и 270 мл молока
        if (water < 30 && water < 300) {
            // Если воды меньше 30 мл, мы точно ничего не приготовим
            break;
        }
        if (water < 300 && milk < 270) {
            // если воды не хватает на американо, а молока на латте
            break;
        }

        int choice = 0;
        std::cout << "\nВыберите напиток (1 — американо, 2 — латте): ";
        std::cin >> choice;

        if (choice == 1) {
            // проверка ингредиентов для американо
            if (water >= 300) {
                water -= 300;
                americanoCount++;
                std::cout << "Ваш напиток готов.\n";
            } else {
                std::cout << "Не хватает воды\n";
            }
        } 
        else if (choice == 2) {
            // проверка ингредиентов для латте
            if (water < 30) {
                std::cout << "Не хватает воды\n";
            } else if (milk < 270) {
                std::cout << "Не хватает молока\n";
            } else {
                water -= 30;
                milk -= 270;
                latteCount++;
                std::cout << "Ваш напиток готов.\n";
            }
        } 
        else {
            std::cout << "Ошибка! Выберите 1 или 2.\n";
        }
    }

    // вывод финального отчёта строго по формату задания
    std::cout << "\n***Отчёт***\n";
    std::cout << "Ингредиентов осталось:\n";
    std::cout << "            Вода: " << water << " мл\n";
    std::cout << "            Молоко: " << milk << " мл\n";
    std::cout << "Кружек американо приготовлено: " << americanoCount << "\n";
    std::cout << "Кружек латте приготовлено: " << latteCount << "\n";

    return 0;
}
