#include <iostream>
#include <cmath> // Библиотека для функции std::abs (модуль числа)

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int m = 0; // Числитель
    int n = 0; // Знаменатель

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Автоматическое сокращение дроби ===\n";
    std::cout << "Введите числитель: ";
    std::cin >> m;
    std::cout << "Введите знаменатель: ";
    std::cin >> n;

    std::cout << "-----Результат-----\n";

    // 1. Контроль ввода: на 0 делить нельзя
    if (n == 0) {
        std::cout << "Ошибка! Знаменатель не может быть равен нулю.\n";
        return 1;
    }

    // Если числитель равен 0, то результат всегда 0
    if (m == 0) {
        std::cout << "Результат: 0\n";
        return 0;
    }

    // 2. Определяем итоговый знак дроби
    // Если только ОДНО из чисел отрицательное — дробь отрицательная
    bool isNegative = (m < 0 && n > 0) || (m > 0 && n < 0);

    // Переводим числа в положительные для работы алгоритма Евклида
    int a = std::abs(m);
    int b = std::abs(n);

    // 3. Быстрый алгоритм Евклида для поиска НОД
    while (a > 0 && b > 0) {
        if (a > b) {
            a %= b;
        } else {
            b %= a;
        }
    }
    // НОД окажется в той переменной, которая не равна нулю
    int nod = (a == 0) ? b : a;

    // 4. Сокращаем числитель и знаменатель на НОД
    int finalM = std::abs(m) / nod;
    int finalN = std::abs(n) / nod;

    // 5. Вывод с учетом правила знаков
    std::cout << "Результат: ";
    if (isNegative) {
        std::cout << "-"; // Ведущий минус строго в начале
    }
    std::cout << finalM << " / " << finalN << "\n";

    return 0;
}
