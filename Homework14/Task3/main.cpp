#include <iostream>

// Настройка русского языка
auto locale_init = std::setlocale(LC_ALL, "ru_RU.UTF-8");

// Рекурсивная функция подсчета чётных цифр согласно чек-листу
void evendigits(long long n, int& ans) {
    // Получаем последнюю цифру числа (берем модуль на случай отрицательного n)
    long long last_digit = std::abs(n % 10);

    // Если цифра чётная, увеличиваем счётчик по ссылке
    if (last_digit % 2 == 0) {
        ans++;
    }

    // Базовый случай: если больше цифр нет, выходим из рекурсии
    if (n / 10 == 0) {
        return;
    }

    // Рекурсивный шаг: вызываем функцию для оставшейся части числа
    evendigits(n / 10, ans);
}

int main() {
    std::cout << "=== Подсчёт чётных цифр в числе (Рекурсия) ===\n";

    // Тестовое число из примера в ТЗ (9223372036854775806)
    long long number = 9223372036854775806LL;
    int ans = 0; // Инициализируем счётчик нулём

    evendigits(number, ans);

    std::cout << "Число: " << number << "\n";
    std::cout << "Количество чётных цифр (ans): " << ans << "\n";

    return 0;
}
