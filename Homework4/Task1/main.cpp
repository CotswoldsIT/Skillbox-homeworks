#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int speed = 0;
    int altitude = 0;

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Система контроля эшелона самолёта ===\n";
    std::cout << "Введите текущую скорость самолёта (км/ч): ";
    std::cin >> speed;
    std::cout << "Введите текущую высоту полёта (метров): ";
    std::cin >> altitude;

    std::cout << "-----Проверяем-----\n";

    // Обработка некорректного ввода (физически невозможные параметры)
    if (speed < 0 || altitude < 0) {
        std::cout << "Ошибка! Скорость и высота не могут быть отрицательными.\n";
        return 1;
    }

    // Проверка эшелона с помощью логического оператора && (И)
    if ((speed >= 750 && speed <= 850) && (altitude >= 9000 && altitude <= 9500)) {
        std::cout << "Полёт нормальный! Самолёт находится в правильном эшелоне.\n";
    } else {
        std::cout << "Внимание! Эшелон нарушен! Измените параметры полёта.\n";
    }

    return 0;
}
