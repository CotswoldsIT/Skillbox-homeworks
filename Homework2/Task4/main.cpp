#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    // Объявляем две переменные (без начальных значений)
    int a = 0;
    int b = 0;

    // Текстовый интерфейс: описание программы
    std::cout << "=== Программа обмена значений без третьей переменной ===\n";
    std::cout << "Приветствуем! Эта программа принимает два числа и меняет их местами с помощью математики.\n\n";

    // Текстовый интерфейс: интерактивный ввод данных
    std::cout << "Введите значение для переменной a: ";
    std::cin >> a;
    std::cout << "Введите значение для переменной b: ";
    std::cin >> b;
    std::cout << "\n";

    // === МАТЕМАТИЧЕСКИЙ АЛГОРИТМ ОБМЕНА МЕСТАМИ ===
    a = a + b; // Шаг 1: в 'a' записываем сумму двух чисел
    b = a - b; // Шаг 2: из суммы вычитаем 'b', получаем исходное 'a' и сохраняем в 'b'
    a = a - b; // Шаг 3: из суммы вычитаем новое 'b', получаем исходное 'b' и сохраняем в 'a'
    // ===============================================

    // Вывод итоговых данных
    std::cout << "--- Результат обмена ---\n";
    std::cout << "Переменная a теперь хранит: " << a << "\n";
    std::cout << "Переменная b теперь хранит: " << b << "\n";
    std::cout << "--------------------------------------------------------\n";

    return 0;
}


