#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int hours = 0;

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Часы с кукушкой ===\n";
    std::cout << "Введите, который час (от 1 до 24): ";
    std::cin >> hours;

    // 1. Контроль ввода (проверка входных значений)
    if (hours < 1 || hours > 24) {
        std::cout << "Ошибка! Введено некорректное время. Допустимый диапазон: 1-24.\n";
        return 1;
    }

    std::cout << "-----Кукушка оживает-----\n";

    // 2. Цикл while для вывода нужного количества фраз
    while (hours > 0) {
        std::cout << "Ку-ку!\n";
        hours--; // Сокращенное уменьшение переменной на 1
    }

    return 0;
}
