#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int startDayOfWeek = 0;
    int day = 0;

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Улучшенный календарь на Май (с любым началом месяца) ===\n\n";
    
    std::cout << "Введите номер дня недели, с которого начинается май\n";
    std::cout << "(1 — Пн, 2 — Вт, 3 — Ср, 4 — Чт, 5 — Пт, 6 — Сб, 7 — Вс): ";
    std::cin >> startDayOfWeek;

    // Проверка корректности ввода дня недели
    if (startDayOfWeek < 1 || startDayOfWeek > 7) {
        std::cout << "Ошибка! Номер дня недели должен быть от 1 до 7.\n";
        return 1;
    }

    std::cout << "Введите номер интересующего дня в мае (от 1 до 31): ";
    std::cin >> day;

    // Проверка корректности ввода числа месяца
    if (day < 1 || day > 31) {
        std::cout << "Ошибка! В мае могут быть только дни с 1 по 31.\n";
        return 1;
    }

    std::cout << "-----Проверяем-----\n";

    // Алгоритм определения дня недели для текущего числа с учетом смещения
    int dayOfWeek = (day + startDayOfWeek - 2) % 7 + 1;

    // Проверяем условия выходного дня:
    // 1. Первые майские праздники: с 1 по 5 включительно
    // 2. Вторые майские праздники: с 8 по 10 включительно
    // 3. Выходные дни недели (6 — суббота, 7 — воскресенье)
    if ((day >= 1 && day <= 5) || (day >= 8 && day <= 10) || (dayOfWeek == 6) || (dayOfWeek == 7)) {
        std::cout << day << " мая — ВЫХОДНОЙ день!\n";
    } else {
        std::cout << day << " мая — РАБОЧИЙ день.\n";
    }

    return 0;
}

