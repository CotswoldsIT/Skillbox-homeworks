#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int dayNumber = 0;

    // Описание программы и ввод данных
    std::cout << "=== Меню бизнес-ланча ресторана ===\n";
    std::cout << "Введите день недели (от 1 до 7): ";
    std::cin >> dayNumber;
    std::cout << "\n";

    // Обработка некорректного ввода
    if (dayNumber < 1 || dayNumber > 7) {
        std::cout << "Ошибка! День недели должен быть числом от 1 до 7.\n";
        return 1; // Завершаем программу с кодом ошибки
    }

    // Определяем день недели и уникальную часть меню
    if (dayNumber == 1) {
        std::cout << "Меню сегодня (понедельник):\n";
        std::cout << "Борщ\n";
        std::cout << "Салат «Витаминный»\n";
        std::cout << "Котлета домашняя с гречкой\n";
    } 
    else if (dayNumber == 2) {
        std::cout << "Меню сегодня (вторник):\n";
        std::cout << "Харчо\n";
        std::cout << "Салат «Оливье»\n";
        std::cout << "Баварские колбаски с капустой\n";
    } 
    else if (dayNumber == 3) {
        std::cout << "Меню сегодня (среда):\n";
        std::cout << "Солянка\n";
        std::cout << "Салат «Греческий»\n";
        std::cout << "Рыбный стейк с рисом\n";
    } 
    else if (dayNumber == 4) {
        std::cout << "Меню сегодня (четверг):\n";
        std::cout << "Гороховый суп\n";
        std::cout << "Салат «Цезарь» с креветками\n";
        std::cout << "Куриная ножка с пюре\n";
    } 
    else if (dayNumber == 5) {
        std::cout << "Меню сегодня (пятница):\n";
        std::cout << "Грибной суп-пюре\n";
        std::cout << "Салат «Мимоза»\n";
        std::cout << "Свиная отбивная с картофелем по-деревенски\n";
    } 
    else if (dayNumber == 6) {
        std::cout << "Меню сегодня (суббота):\n";
        std::cout << "Уха из лосося\n";
        std::cout << "Салат «Крабовый»\n";
        std::cout << "Паста Карбонара\n";
    } 
    else if (dayNumber == 7) {
        std::cout << "Меню сегодня (воскресенье):\n";
        std::cout << "Сырный крем-суп\n";
        std::cout << "Теплый салат с говядиной\n";
        std::cout << "Шашлык из курицы с лавашом\n";
    }

    // Общая часть меню для любого дня (вынесена отдельно, чтобы не дублировать код)
    std::cout << "Морс\n";

    return 0;
}
