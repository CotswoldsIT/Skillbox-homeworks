#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    // Начальные координаты марсохода в центре комнаты 15x20
    int x = 7;
    int y = 10;

    std::cout << "=== Система управления марсоходом «Прототип-1» ===\n";
    std::cout << "Размер комнаты: 15x20 метров. Центр: 7, 10.\n";
    std::cout << "Команды: W — Север, S — Юг, A — Запад, D — Восток.\n\n";

    // зацикливание симуляции
    while (true) {
        std::cout << "[Программа]: Марсоход находится на позиции " << x << ", " << y << ", введите команду: ";
        char command;
        std::cin >> command;

        // Переводим символ в верхний регистр на случай, если ввели маленькую букву
        command = std::toupper(command);

        // Обработка команд движения с помощью else if и else
        if (command == 'W') {
            // Север (вверх по Y)
            if (y < 20) {
                y++;
            } else {
                std::cout << "[Предупреждение]: Впереди стена! Движение на Север невозможно.\n";
            }
        } 
        else if (command == 'S') {
            // Юг (вниз по Y)
            if (y > 0) {
                y--;
            } else {
                std::cout << "[Предупреждение]: Впереди стена! Движение на Юг невозможно.\n";
            }
        } 
        else if (command == 'A') {
            // Запад (влево по X)
            if (x > 0) {
                x--;
            } else {
                std::cout << "[Предупреждение]: Впереди стена! Движение на Запад невозможно.\n";
            }
        } 
        else if (command == 'D') {
            // Восток (вправо по X)
            if (x < 15) {
                x++;
            } else {
                std::cout << "[Предупреждение]: Впереди стена! Движение на Восток невозможно.\n";
            }
        } 
        else {
            // Контроль некорректного ввода
            std::cout << "[Ошибка]: Неверная команда! Используйте только W, A, S, D.\n";
        }
        
        std::cout << "\n";
    }

    return 0;
}
