#include <iostream>

// Настройка русского языка
auto locale_init = std::setlocale(LC_ALL, "ru_RU.UTF-8");

// Рекурсивная функция расчета способов со значением по умолчанию k = 3 по ТЗ
int count_ways(int n, int k = 3) {
    // Базовые случаи рекурсии
    if (n == 0) return 1; // Допрыгал ровно до цели
    if (n < 0) return 0;  // Перелёт или некорректный шаг

    int total_ways = 0;

    // Суммируем способы со всех возможных предыдущих ступенек
    for (int i = 1; i <= k; ++i) {
        total_ways += count_ways(n - i, k);
    }

    return total_ways;
}

int main() {
    std::cout << "=== Расчёт путей кролика (Рекурсия) ===\n";

    // Тест 1: Из примера ТЗ (n = 3, k = 2)
    int n1 = 3, k1 = 2;
    std::cout << "Ступенек: " << n1 << ", Макс. прыжок: " << k1 << " -> Способов: " << count_ways(n1, k1) << "\n";

    // Тест 2: Проверка аргумента по умолчанию (k = 3) для 3 ступенек
    int n2 = 3;
    std::cout << "Ступенек: " << n2 << ", Макс. прыжок (по умолчанию): -> Способов: " << count_ways(n2) << "\n";

    return 0;
}
