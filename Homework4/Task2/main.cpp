#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int day = 0;

    // Текстовый интерфейс: описание и ввод данных
    std::cout << "=== Календарь выходных дней на Май ===\n";
    std::cout << "Введите номер дня в мае (от 1 до 31): ";
    std::cin >> day;

    std::cout << "-----Проверяем-----\n";

    // Обработка некорректного ввода (в мае только 31 день)
    if (day < 1 || day > 31) {
        std::cout << "Ошибка! В мае могут быть только дни с 1 по 31.\n";
        return 1;
    }

    // Объединяем условия выходных дней с помощью логического ИЛИ (||)
    // 1. Первые майские праздники: с 1 по 5 включительно
    // 2. Вторые майские праздники: с 8 по 10 включительно
    // 3. Выходные дни (суббота — остаток 6 от деления на 7, воскресенье — остаток 0)
    if ((day >= 1 && day <= 5) || (day >= 8 && day <= 10) || (day % 7 == 6) || (day % 7 == 0)) {
        std::cout << day << " мая — ВЫХОДНОЙ день! Отдыхаем.\n";
    } else {
        std::cout << day << " мая — РАБОЧИЙ день. Пора за работу.\n";
    }

    return 0;
}
