#include <iostream>
#include <string>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    std::string debtorName = "";
    int debtAmount = 0;

    std::cout << "=== Робот-коллектор «Честный заём» ===\n\n";
    std::cout << "Введите имя должника: ";
    std::cin >> debtorName;
    std::cout << "Введите сумму долга (руб.): ";
    std::cin >> debtAmount;

    // 1. Контроль ввода: при отрицательном долге прекращаем работу
    if (debtAmount < 0) {
        std::cout << "Ошибка! Сумма долга не может быть отрицательной.\n";
        return 1;
    }

    std::cout << "\n----- Робот начинает работу -----\n";

    // Если долг изначально равен 0, то сразу выходим
    if (debtAmount == 0) {
        std::cout << debtorName << ", у вас нет задолженности. Всего доброго!\n";
        return 0;
    }

    // 2. Бесконечный цикл с выходом через break
    while (true) {
        std::cout << debtorName << ", ваш текущий долг составляет: " << debtAmount << " руб.\n";
        std::cout << "Какую сумму вы готовы внести прямо сейчас? ";
        
        int payment = 0;
        std::cin >> payment;

        // Контроль ввода платежа
        if (payment < 0) {
            std::cout << "Ошибка! Сумма платежа не может быть отрицательной. Попробуйте снова.\n\n";
            continue; // Возвращает в начало цикла, пропуская расчеты ниже
        }

        // Уменьшаем сумму долга
        debtAmount -= payment;

        // 3. Проверка на полное погашение долга
        if (debtAmount == 0) {
            std::cout << "\nДолг полностью погашен! Спасибо за сотрудничество.\n";
            break; // Останавливает цикл намертво
        } 
        else if (debtAmount < 0) {
            // Если долг ушел в минус, вычисляем остаток на счету
            int balance = debtAmount * -1;
            std::cout << "\nДолг полностью погашен! Спасибо за сотрудничество.\n";
            std::cout << "Ваш остаток на счету после операции: " << balance << " руб.\n";
            break;
        }

        std::cout << "Платеж принят. Остаток задолженности обновлен.\n\n";
    }

    std::cout << "--------------------------------------------------------\n";
    return 0;
}
