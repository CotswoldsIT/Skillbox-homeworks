#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    int a = 42;
    int b = 153;

    std::cout << "--- До обмена ---\n";
    std::cout << "a: " << a << "\n"; 
    std::cout << "b: " << b << "\n\n"; 

    // === АЛГОРИТМ ОБМЕНА МЕСТАМИ ===
    // 1. Сохраняем значение 'a' во временную переменную
    int temp = a; 
    
    // 2. Записываем в переменную 'a' значение из 'b'
    a = b; 
    
    // 3. В 'b' записываем старое значение 'a', которое лежит в 'temp'
    b = temp; 
    // ===============================

    std::cout << "--- После обмена ---\n";
    std::cout << "a: " << a << "\n"; 
    std::cout << "b: " << b << "\n"; 

    return 0;
}
