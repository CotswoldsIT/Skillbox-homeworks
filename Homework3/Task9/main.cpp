#include <iostream>

int main() {
    // Настройка русского языка
    std::setlocale(LC_ALL, "ru_RU.UTF-8");

    double income = 0.0;
    double tax = 0.0;

    std::cout << "=== Расчёт прогрессивного налога (честная шкала) ===\n";
    std::cout << "Введите ваш годовой доход (руб.): ";
    std::cin >> income;

    std::cout << "-----Считаем-----\n";

    // Обработка некорректного ввода
    if (income < 0) {
        std::cout << "Ошибка! Доход не может быть отрицательным.\n";
        return 1;
    }

    // Расчёт налога по слоям с помощью оптимальной структуры else if
    if (income > 50000) {
        // 13% с первых 10к (1300) + 20% со слоя от 10к до 50к (8000) + 30% с остатка
        tax = 1300.0 + 8000.0 + (income - 50000.0) * 0.30;
    } 
    else if (income > 10000) {
        // 13% с первых 10к (1300) + 20% со всего, что выше 10к
        tax = 1300.0 + (income - 10000.0) * 0.20;
    } 
    else {
        // Только 13% для небольшого дохода
        tax = income * 0.13;
    }

    std::cout << "Сумма прогрессивного налога к уплате: " << tax << " руб.\n";

    return 0;
}
